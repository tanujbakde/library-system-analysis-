SELECT m.member_id, 
	m.member_name, 
	ist.issued_book_name,
	COUNT(*)
FROM return_status AS rs
JOIN issued_status AS ist ON rs.issued_id = ist.issued_id
JOIN members AS m ON m.member_id = ist.issued_member_id
WHERE rs.book_quality = 'Damaged'
GROUP BY 1, 3 
HAVING COUNT(*) > 2;


